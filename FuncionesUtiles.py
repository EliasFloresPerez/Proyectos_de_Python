print("...".join(["spam", "eggs", "ham"]))
#prints "spam, eggs, ham" Join une una lista con una cadena la primera como separador

print("Hello ME".replace("ME", "world"))
#prints "Hello world" Replace Remplaza una subcadena con otra 

print("This is a sentence.".startswith("This"))
# prints "True" Star mira si una subcadena se encuentra al principio

print("This is a sentence.".endswith("sentence."))
# prints "True" End mira si la subcadena se encuentra al final 

print("This is a sentence.".upper())
# prints "THIS IS A SENTENCE." Upper convierte una cadena a mayusculas

print("AN ALL CAPS SENTENCE".lower())
#prints "an all caps sentence" Lower la transforma a minusculas

